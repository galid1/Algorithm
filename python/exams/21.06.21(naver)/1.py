def solution(A):
    ans = 0

    i = 0
    while i < len(A)-2:
        diff = A[i] - A[i+1]
        contain_cnt = 2
        for j in range(i+1, len(A)-1):
            if A[j] - A[j+1] == diff:
                contain_cnt += 1
            else:
                break

        if contain_cnt >= 3:
            k = 2
            section_cnt = 0
            while contain_cnt - k >= 1:
                section_cnt += contain_cnt - k
                k += 1
            ans += section_cnt
            i += (contain_cnt - 1)
        else:
            i += 1

    # 10억 초과시 -1반환
    if ans > 1000000000:
        return -1
    else:
        return ans

